        "import json\n",
        "import os\n",
        "import matplotlib.pyplot as plt\n",
        "from datetime import datetime\n",
        "\n",
        "FILE_NAME = \"expenses.json\"\n",
        "\n",
        "# Load existing data\n",
        "def load_expenses():\n",
        "    if os.path.exists(FILE_NAME):\n",
        "        with open(FILE_NAME, \"r\") as file:\n",
        "            return json.load(file)\n",
        "    return []\n",
        "\n",
        "# Save data\n",
        "def save_expenses(expenses):\n",
        "    with open(FILE_NAME, \"w\") as file:\n",
        "        json.dump(expenses, file, indent=4)\n",
        "\n",
        "# Add expense\n",
        "def add_expense(expenses):\n",
        "    try:\n",
        "        amount = float(input(\"Enter amount: \"))\n",
        "    except ValueError:\n",
        "        print(\"Invalid amount.\\n\")\n",
        "        return\n",
        "\n",
        "    category = input(\"Enter category (food, travel, etc.): \")\n",
        "    note = input(\"Enter note: \")\n",
        "\n",
        "    date_input = input(\"Enter date (YYYY-MM-DD) or press Enter for today: \")\n",
        "\n",
        "    if date_input == \"\":\n",
        "        date = datetime.today().strftime(\"%Y-%m-%d\")\n",
        "    else:\n",
        "        try:\n",
        "            datetime.strptime(date_input, \"%Y-%m-%d\")\n",
        "            date = date_input\n",
        "        except ValueError:\n",
        "            print(\"Invalid date format.\\n\")\n",
        "            return\n",
        "\n",
        "    expense = {\n",
        "        \"amount\": amount,\n",
        "        \"category\": category,\n",
        "        \"note\": note,\n",
        "        \"date\": date\n",
        "    }\n",
        "\n",
        "    expenses.append(expense)\n",
        "    save_expenses(expenses)\n",
        "    print(\"Expense added successfully.\\n\")\n",
        "\n",
        "# View all expenses\n",
        "def view_expenses(expenses):\n",
        "    if not expenses:\n",
        "        print(\"No expenses found.\\n\")\n",
        "        return\n",
        "\n",
        "    for i, exp in enumerate(expenses, start=1):\n",
        "        print(f\"{i}. ₹{exp['amount']} | {exp['category']} | {exp['note']} | {exp['date']}\")\n",
        "    print()\n",
        "\n",
        "# Total spending\n",
        "def total_spending(expenses):\n",
        "    total = sum(exp[\"amount\"] for exp in expenses)\n",
        "    print(f\"Total Spending: ₹{total}\\n\")\n",
        "\n",
        "# Category-wise summary\n",
        "def category_summary(expenses):\n",
        "    summary = {}\n",
        "\n",
        "    for exp in expenses:\n",
        "        cat = exp[\"category\"]\n",
        "        summary[cat] = summary.get(cat, 0) + exp[\"amount\"]\n",
        "\n",
        "    print(\"Category-wise Spending:\")\n",
        "    for cat, amt in summary.items():\n",
        "        print(f\"{cat}: ₹{amt}\")\n",
        "    print()\n",
        "\n",
        "# Plot graph\n",
        "def plot_expenses(expenses):\n",
        "    if not expenses:\n",
        "        print(\"No data to plot.\\n\")\n",
        "        return\n",
        "\n",
        "    summary = {}\n",
        "\n",
        "    for exp in expenses:\n",
        "        cat = exp[\"category\"]\n",
        "        summary[cat] = summary.get(cat, 0) + exp[\"amount\"]\n",
        "\n",
        "    categories = list(summary.keys())\n",
        "    amounts = list(summary.values())\n",
        "\n",
        "    plt.bar(categories, amounts)\n",
        "    plt.xlabel(\"Categories\")\n",
        "    plt.ylabel(\"Amount (₹)\")\n",
        "    plt.title(\"Expense Distribution\")\n",
        "\n",
        "    plt.show()\n",
        "\n",
        "# Filter by date\n",
        "def filter_by_date(expenses):\n",
        "    date = input(\"Enter date (YYYY-MM-DD): \")\n",
