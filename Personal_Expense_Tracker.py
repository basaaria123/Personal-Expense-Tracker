        "            category_summary(expenses)\n",
        "        elif choice == \"5\":\n",
        "            plot_expenses(expenses)\n",
        "        elif choice == \"6\":\n",
        "            filter_by_date(expenses)\n",
        "        elif choice == \"7\":\n",
